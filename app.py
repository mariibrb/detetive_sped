def _bootstrap_bundled() -> None:
    sl = types.ModuleType("spedlib")
    sl.__path__ = []
    sl.__name__ = "spedlib"
    sl.__package__ = "spedlib"
    sys.modules["spedlib"] = sl
    exec(compile(_dec(_B_INIT), "spedlib/__init__.py", "exec"), sl.__dict__)
    _load("spedlib.utils", _dec(_B_UTILS), "spedlib")
    efd = _dec(_B_EFD).replace("from .utils import", "from spedlib.utils import")
    _load("spedlib.efd_reader", efd, "spedlib")
